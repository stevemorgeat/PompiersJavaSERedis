    private void remplirJCombo() {
        long liTaille = jedis.zcard("Categories");
        Set<String> setCategories = jedis.zrange("Categories", 0, liTaille);
        for (String lsCategorie : setCategories) {
            jComboBoxCategories.addItem(lsCategorie);
        }
    } /// remplirJCombo

