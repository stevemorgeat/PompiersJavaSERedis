    private void jButtonCommitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCommitActionPerformed
        //
        jLabelMessage.setText("");
        Map<String, String> mapPersonne;
        try {
            int liRows = jTablePersonnes.getRowCount();
            String lsFlag;
            String lsPersonne;
            String lsCategorie;

            for (int i = liRows - 1; i >= 0; i--) {
                lsFlag = jTablePersonnes.getValueAt(i, 0).toString();
                lsPersonne = jTablePersonnes.getValueAt(i, 1).toString();
                lsCategorie = jTablePersonnes.getValueAt(i, 2).toString();

                if (lsFlag.equals("+")) {
                    mapPersonne = new LinkedHashMap();
                    mapPersonne.put(lsPersonne, lsCategorie);
                    jedis.hmset("Personnes", mapPersonne);
                    idtm.setValueAt("", i, 0);
                }
                if (lsFlag.equals("-")) {
                    jedis.hdel("Personnes", lsPersonne);
                    idtm.removeRow(i);
                }
                if (lsFlag.equals("v")) {
                    // Suppression puis Ajout
                    jedis.hdel("Personnes",lPersonnesInit.get(i)[1].toString());
                    jedis.hset("Personnes", lsPersonne, lsCategorie);
                            // Il faut avoir gardé la valeur "primitive"
                            //jedis.hmset("Personnels", lsPersonnel);
                    idtm.setValueAt("", i, 0);
                }
            }
            jLabelMessage.setText("Validation définitive OK");
        } catch (Exception e) {
            jLabelMessage.setText(e.getMessage());
        }
    }//GEN-LAST:event_jButtonCommitActionPerformed

