    private void jButtonAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAjouterActionPerformed
        String lsPersonne = jTextFieldPersonne.getText();
        String lsCategorie = jComboBoxCategories.getSelectedItem().toString();

        jLabelMessage.setText("");

        if (lsPersonne.trim().isEmpty()) {
            jLabelMessage.setText("Saisies obligatoires");
        } else {
            String[] tLigne = new String[3];
            tLigne[0] = "+";
            tLigne[1] = lsPersonne;
            tLigne[2] = lsCategorie;
            idtm.addRow(tLigne);
        }
    }//GEN-LAST:event_jButtonAjouterActionPerformed

